create extension if not exists pgcrypto;

do $$ begin
  create type public.panora_role as enum ('admin', 'restaurant');
exception when duplicate_object then null;
end $$;

do $$ begin
  create type public.order_status as enum ('submitted', 'confirmed', 'shipped', 'cancelled');
exception when duplicate_object then null;
end $$;

do $$ begin
  create type public.payment_status as enum ('pending', 'confirmed', 'cancelled');
exception when duplicate_object then null;
end $$;

create table if not exists public.restaurants (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  email text not null,
  phone text,
  telegram text,
  address text,
  language text not null default 'ru' check (language in ('ru','en','es')),
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists restaurants_email_unique on public.restaurants (lower(email));

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  role public.panora_role not null,
  restaurant_id uuid references public.restaurants(id) on delete set null,
  display_name text,
  created_at timestamptz not null default now(),
  constraint restaurant_profile_has_restaurant check (
    (role = 'admin' and restaurant_id is null) or
    (role = 'restaurant' and restaurant_id is not null)
  )
);

create table if not exists public.products (
  id text primary key,
  name_ru text not null,
  name_en text not null,
  name_es text not null,
  description_ru text not null default '',
  description_en text not null default '',
  description_es text not null default '',
  weight_g integer not null default 750 check (weight_g > 0),
  base_price numeric(10,2) not null default 0 check (base_price >= 0),
  image_url text,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.restaurant_prices (
  restaurant_id uuid not null references public.restaurants(id) on delete cascade,
  product_id text not null references public.products(id) on delete cascade,
  price numeric(10,2) not null check (price >= 0),
  updated_at timestamptz not null default now(),
  primary key (restaurant_id, product_id)
);

create table if not exists public.bake_days (
  id uuid primary key default gen_random_uuid(),
  bake_date date not null unique,
  delivery_date date not null,
  cutoff_at timestamptz not null,
  accepting_orders boolean not null default true,
  cancellation_reason text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.bake_items (
  bake_day_id uuid not null references public.bake_days(id) on delete cascade,
  product_id text not null references public.products(id) on delete cascade,
  planned_quantity integer not null default 0 check (planned_quantity >= 0),
  primary key (bake_day_id, product_id)
);

create table if not exists public.orders (
  id uuid primary key default gen_random_uuid(),
  order_number bigint generated by default as identity unique,
  restaurant_id uuid not null references public.restaurants(id),
  bake_day_id uuid not null references public.bake_days(id),
  status public.order_status not null default 'submitted',
  comment text,
  cancelled_reason text,
  created_by uuid references auth.users(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.order_items (
  id uuid primary key default gen_random_uuid(),
  order_id uuid not null references public.orders(id) on delete cascade,
  product_id text not null references public.products(id),
  quantity integer not null check (quantity > 0),
  unit_price numeric(10,2) not null check (unit_price >= 0),
  unique (order_id, product_id)
);

create table if not exists public.delivery_notes (
  id uuid primary key default gen_random_uuid(),
  note_number bigint generated by default as identity unique,
  order_id uuid not null unique references public.orders(id),
  restaurant_id uuid not null references public.restaurants(id),
  delivered_at timestamptz not null default now(),
  total numeric(12,2) not null check (total >= 0),
  qr_token uuid not null default gen_random_uuid() unique,
  customer_confirmed_at timestamptz,
  customer_receiver text,
  created_at timestamptz not null default now()
);

create table if not exists public.payments (
  id uuid primary key default gen_random_uuid(),
  restaurant_id uuid not null references public.restaurants(id),
  delivery_note_id uuid references public.delivery_notes(id) on delete set null,
  amount numeric(12,2) not null check (amount > 0),
  method text not null,
  note text,
  status public.payment_status not null default 'pending',
  received_at timestamptz not null default now(),
  confirmed_at timestamptz,
  confirmed_by uuid references auth.users(id),
  created_at timestamptz not null default now()
);

create or replace function public.panora_is_admin()
returns boolean language sql stable security definer set search_path = public
as $$ select exists(select 1 from public.profiles p where p.id = auth.uid() and p.role = 'admin') $$;

create or replace function public.panora_restaurant_id()
returns uuid language sql stable security definer set search_path = public
as $$ select p.restaurant_id from public.profiles p where p.id = auth.uid() $$;

